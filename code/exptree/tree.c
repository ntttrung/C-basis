TreeNode *searchBST(tree t, element_t x) {
  if (isNullTree(t))
    return NULL;
  
  if (value(t) == x)
    return t;
  else if (value(t) < x)
    return searchBST(right(t));
  else
    return searchBST(left(t));
}

tree insertBST(tree t, element_t x) {
  if (isNullTree(t))
    return createLeaf(x);

  if (value(t) == x)
    return t;
  else if (value(t) > x) {
    t->left = insertBST(t->left, x);
    return t;
  } else {
    t->right = insertBST(t->right, x);
    return t;
  }
}
